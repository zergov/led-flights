class Aircraft:
    """
    Represents an aircraft detected by dump1090.

    The `data` object is the JSON object that represents an aircraft in the
    `aircraft.json` file generated by dump1090.
    """

    def __init__(self, data, db_data):
        self.data = data
        self.db_data = db_data

    def icao_hex(self) -> str:
        return self.data.get("hex").upper()

    def flight(self):
        return self.data.get("flight", None)

    def altitude(self):
        return self.data.get("alt_baro", None)

    def squawk(self):
        return self.data.get("squawk", None)

    def registration(self):
        return self.db_data.get("registration", None)

    def aircraft_type(self):
        return self.db_data.get("type", None)

    def name(self):
        return self.db_data.get("name", None)
